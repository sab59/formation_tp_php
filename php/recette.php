        <h4>Tarte framboise meuringué</h4>

        <div class="recette3">
        <div><img class="img-recette-l" src="../img/recette3.PNG" alt="Tarte framboise meuringué" width="250" height="200"></div>
        <div class="ing">
        100 g beurre pommade
80 g sucre glace
1 g oeuf entier
3 g sel fin
200 g farine
4 oeufs
100 g sucre en poudre
10 g maïzena
150 g framboises
130 g beurre
        </div>
        <div class="recette">
        Dans une casserole, faites chauffer les framboises et passez les au mixeur plongeant.
Passez ensuite cette purée de framboise au chinois pour éliminer les petits grains.
Dans un cul de poule, fouettez vivement les oeufs entiers et le sucre en poudre. 
Ajoutez la maïzena et mélangez bien, veillez à ne pas faire de grumeaux.
Versez la purée de framboise sur le mélange précédent tout en remuant.
Reversez le tout dans  la casserole et faites chauffer en remuant jusqu'à la première ébullition de la crème framboise.
Quand la crème est cuite, hors du feu, ajoutez le beurre en morceaux et lissez au mixeur plongeant.
Versez la crème framboise dans le fond de tarte, lissez la surface à la spatule et réservez au froid.
        </div>
