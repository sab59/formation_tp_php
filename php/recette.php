        <h4>Charlotte aux fraises</h4>

        <div class="recette4">
        <div><img class="img-recette-r" src="../img/recette4.PNG" alt="Charlotte aux fraises" width="250" height="200"></div>
        <div class="ing">
        120 g blancs d'oeufs
100 g sucre en poudre
80 g jaunes d'oeufs
100 g farine
sucre glace
200 g chocolat blanc de couverture
100 ml crème liquide entière
120 g blancs d'oeufs
200 g fraises
        </div>
        <div class="recette">
        Au bain-marie ou au micro-ondes, faites fondre doucement le chocolat blanc.
Dans une casserole, portez la crème liquide à ébullition.
Versez la crème liquide chaude sur le chocolat blanc et mélangez vivement.
Réservez.
Montez les blancs en neige ferme.
Versez le mélange au chocolat blanc sur les blancs montés et mélangez délicatement à la maryse.
Versez la mousse au chocolat blanc dans une poche à douille sans douille, réservez.
Posez le cercle de 20cm de diamètre et 4,5cm de hauteur sur le plat de service ou sur un carton or. Il est inutile de le chemiser de rhodoïd.
Coupez les cartouchières à la base pour que les biscuits fassent 5,5cm de hauteur puis placez les dans le cercle, le côté poudré de sucre glace contre le cercle.
Découpez le disque de biscuit cuiller à 20cm de diamètre et posez le, côté poudré vers le plat, à l'intérieur du cercle.
Votre montage doit être parfaitement hermétique sinon la bavaroise va s'échapper.
