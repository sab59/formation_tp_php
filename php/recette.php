        <h4>Fondant banane chocolat</h4>

        <div class="recette1">
        <div class="image-recette"><img class="img-recette-l" src="../img/recette1.PNG" alt="gateau fondant banane chocolat" width="250" height="200"></div>
        <div class="ing">
            2 bananes très mûres
3 oeufs
170 g chocolat noir
100 g beurre
1 pincée fleur de sel
1 cuillère à café extrait naturel de vanille liquide
120 g farine
50 g pépites de chocolat</div>
        <div class="recette">Préchauffez le four à 170° (th5/6) chaleur tournante et graissez l'intérieur d'un moule en silicone de 20cm de diamètre.
Dans le bol d'un blender ou d'un robot muni d'une lame, versez les bananes pelées et coupées en rondelle puis les oeufs et mixez.
Au bain-marie ou au micro-ondes, faites fondre le chocolat noir avec le beurre et mélangez bien.
Versez ce mélange dans le blender, ajoutez la fleur de sel puis l'extrait de vanille et mixez à nouveau.
Ajoutez enfin la farine, mixez encore et versez l'appareil obtenu dans le moule.
Ajoutez les pépites de chocolat et enfournez pour 50 minutes de cuisson.
A la sortie du four, attendez le refroidissement complet avant de démouler le fondant banane chocolat sur le plat de service.</div>

