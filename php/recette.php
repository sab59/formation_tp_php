        <h4>Cake vanille insert chocolat</h4>

        <div class="recette5">
        <div><img class="img-recette-l" src="../img/recette5.PNG" alt="Cake vanille insert chocolat" width="250" height="200"></div>
        <div class="ing">
        120 g beurre pommade
200 g sucre en poudre
1 gousse vanille
2 oeufs
200 ml crème liquide entière
220 g farine
4 g levure chimique
        </div>
        <div class="recette">
        Au robot muni de la feuille (fouet plat) mélangez le beurre pommade avec le sucre en poudre et les graines de la gousse de vanille fendue et grattée.
Ajoutez les oeufs et fouettez vivement.
Versez la crème liquide et fouettez à nouveau.
Enfin, ajoutez la farine et la levure chimique et fouttez doucement jusqu'à obtenir un appareil homogène.
Préchauffez le four à 145° (th4/5) chaleur tournante et graissez l'intérieur, le tube et le couvercle du moule à cake spécial insert.
Versez la pâte dans le moule, tapez le sur le plan de travail pour qu'elle remplisse bien les coins du moule.
