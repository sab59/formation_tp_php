        <h4>Brownie chocolat beurre de cacahuéte</h4>

        <div class="recette2">
        <div><img class="img-recette-r" src="../img/recette2.PNG" alt="Brownie chocolat et beurre de cacahuéte" width="250" height="200"></div>
        <div class="ing">
        170 g chocolat noir
130 g beurre
1/2 cuillère à café vanille en poudre
1 pincée fleur de sel
3 oeufs
120 g farine
2 g levure chimique
200 g beurre de cacahuètes
        </div>
        <div class="recette">
        Préchauffez le four à 170° (th5/6) chaleur tournante et graissez un plat en verre carré de 20cm de côté.
Faites fondre le chocolat avec le beurre et mélangez bien pour obtenir un appareil bien lisse.
Ajoutez la fleur de sel et la vanille en poudre et mélangez.
Incorporez les oeufs un par un en mélangeant bien entre chaque ajout.
Ajoutez la farine et la levure chimique et mélangez à nouveau.
Versez l'appareil à brownie dans le plat, ajoutez le beurre de cacahuètes par petites touches pour donner un effet marbré et enfournez pour 25 minutes.
        </div>
