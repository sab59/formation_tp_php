        <h4>Tarte aux kiwi</h4>

        <div class="recette6">
        <div><img class="img-recette-r" src="../img/recette6.PNG" alt="Tarte aux kiwi" width="250" height="200"></div>
        <div class="ing">
        50 g beurre pommade
50 g sucre en poudre
50 g poudre d'amande
1 oeuf
2 jaunes d'oeufs
70 g sucre en poudre
30 g fécule de maïs
1 gousse vanille
300 ml lait
        </div>
        <div class="recette">
        Dans un cul de poule, mélangez le beurre pommade avec le sucre en poudre.
Ajoutez l'oeuf puis la poudre d'amandes et mélangez à nouveau.
Mélangez doucement pour ne pas trop incorporer d'air, la crème d'amande a tendance à gonfler à la cuisson et il doit vous rester de la place pour la ganache fraise.
Garnissez le fond de tarte avec la crème d'amande et réservez au frais.
Préchauffez le four à 170° (th5/6) chaleur tournante et quand il est chaud enfournez le fond de tarte pour 30 à 40 minutes de cuisson.
La durée de cuisson varie selon les fours, la pâte et la crème d'amande doivent être bien dorées.
